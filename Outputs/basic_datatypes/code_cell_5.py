# Program to perform arithmetic operations on numeric variables of integer and float data types
a = 5
b = 2.5
c = a + b # addition
d = a - b # subtraction
e = a * b # multiplication
f = a / b # division
g = a ** b # exponentiation
h = a % b # modulus
i = a // b # floor division
print(c, d, e, f, g, h, i)